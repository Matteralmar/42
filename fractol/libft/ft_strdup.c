#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	size_t	s_l;
	size_t	i;

	s_l = ft_strlen(s1);
	dup = (char *)malloc(s_l + 1);
	if (!dup)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
