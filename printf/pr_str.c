#include "ft_printf.h"

size_t	pr_str(char *s)
{
	size_t	i;

	if (!s)
		return (pr_str("(null)"));
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
